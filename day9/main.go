type head struct {
	sym  string
	x, y int
}

func (t *head) adjust(h *head) {
	if h.x == t.x {
		if t.y-h.y > 1 {
			t.y--
			return
		}
		if t.y-h.y < -1 {
			t.y++
			return
		}

		// else do nothing
		return
	}

	if h.y == t.y {
		if t.x-h.x > 1 {
			t.x--
			return
		}
		if t.x-h.x < -1 {
			t.x++
			return
		}

		return
	}

	// adjacency
	if h.x-t.x == 1 || h.x-t.x == -1 {
		if h.y-t.y > 1 {
			t.y++
			t.x = h.x
			return
		}
		if h.y-t.y < -1 {
			t.y--
			t.x = h.x
			return
		}
		return
	}

	// adjacency
	if h.y-t.y == 1 || h.y-t.y == -1 {
		if h.x-t.x > 1 {
			t.x++
			t.y = h.y
			return
		}
		if h.x-t.x < -1 {
			t.x--
			t.y = h.y
			return
		}
		return
	}
	// a special case
	if h.x-t.x == 2 || h.x-t.x == -2 {
		if h.y-t.y > 1 {
			t.y++
		}
		if h.y-t.y < -1 {
			t.y--
		}

		if h.x-t.x > 0 {
			t.x = h.x - 1
		} else {
			t.x = h.x + 1
		}
	}

}

